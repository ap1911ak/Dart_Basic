// ข้อ 9: แปลงอุณหภูมิ
// โจทย์: เขียนโปรแกรมแปลงอุณหภูมิจากองศาเซลเซียส (Celsius) เป็นฟาเรนไฮต์ (Fahrenheit) โดยกำหนดค่าเซลเซียสในตัวแปร

// ข้อจำกัด:

// ใช้สูตร: F = (C * 9/5) + 32

// ตัวแปรอุณหภูมิต้องเป็น double

void main(){
 double c = 32, f = (c*9/5)+32;
  print("$c Celsius degree is $f Fahrenheit degree");
}
